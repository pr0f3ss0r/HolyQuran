package com.tosindroid.pr0f.holyquran;

public class JuzPart {
    private String juz, juzSurahName, juzNumber, juzPageNumber;

    public String getJuz() {
        return juz;
    }

    public void setJuz(String juz) {
        this.juz = juz;
    }

    public String getJuzSurahName() {
        return juzSurahName;
    }

    public void setJuzSurahName(String juzSurahName) {
        this.juzSurahName = juzSurahName;
    }

    public String getJuzNumber() {
        return juzNumber;
    }

    public void setJuzNumber(String juzNumber) {
        this.juzNumber = juzNumber;
    }

    public String getJuzPageNumber() {
        return juzPageNumber;
    }

    public void setJuzPageNumber(String juzPageNumber) {
        this.juzPageNumber = juzPageNumber;
    }
}
